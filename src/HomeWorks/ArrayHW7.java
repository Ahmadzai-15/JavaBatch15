package HomeWorks;

public class ArrayHW7 {
    public static void main(String[] args) {

       /* 6) Create an array of countries. While retrieving all values
        from an array print capital for each country choose any five countries.*/

        String[] country={"AFGHANISTAN","USA","Turkey","India","UK","pakistan"};


        switch(country[0]){
            case "AFGHANISTAN":
                System.out.println("KABUL");
                break;
            case "USA":
                System.out.println("Washington DC");
                break;
            case"Turkey":
                System.out.println("Aqara");
                break;
            case"India":
                System.out.println("Delhi");
                break;
            case"UK":
                System.out.println("London");
                break;
            case"pakistan":
                System.out.println("Islamabad");
                break;




        }


    }
}
